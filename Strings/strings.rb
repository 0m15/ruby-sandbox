# Operations with strings

# hello ruby
puts "hello ruby"

# hello ruby
puts 'hello ruby'

# hello\ruby
puts "hello\ruby"

# hello\\ruby
puts 'hello\ruby'

# the amount is 20
puts "the amount is #{5*4}"

# the amount is #{3*2}
puts 'the amount is #{3*2}'

# will it rocks?
var = "rocks?"
puts "will it #{var}"

# it will
puts "it" + "will"

# it will it will
puts "it will " * 2

# r
puts "ruby"[0]

# true
puts "true" == "true"